package fr.inria.corese.compiler.java;

import java.util.Date;

/**
 *
 * @author Olivier Corby, Wimmics INRIA I3S, 2017
 *
 */
public class Header {
    static final String NL = System.getProperty("line.separator");
    
 static final String importList = 
              "import fr.inria.acacia.corese.api.IDatatype;\n"
            + "import fr.inria.edelweiss.kgraph.query.PluginImpl;\n"
            + "import fr.inria.acacia.corese.cg.datatype.DatatypeMap;\n";  
 
 static final String comment  = 
         "/**\n"
         + " * Code generated by LDScript Java compiler for extension functions \n"
         + " * Compiler is run by annotation: @compile <DataShape> \n"
         + " * functions are called with: \n"
         + " * prefix java: <function://fr.inria.corese.extension.DataShape> \n"
         + " * java:fun(?x) \n"
         + " *\n"
         + " * Olivier Corby - Wimmics Inria I3S - %s \n"
         + " */";
  
 
    JavaCompiler jc;
    StringBuilder sb ;
    
    Header(JavaCompiler jc){
        this.jc = jc;
        sb = new StringBuilder();
    }
    
    void process(String name) {
        sb.append("package fr.inria.corese.extension;");
        nl();
        nl();
        sb.append(importList);      
        nl();
        nl();
        sb.append(String.format(comment, new Date()));
        nl();
        sb.append(String.format("public class %s extends PluginImpl { ", name));
        nl();
        nl();          
    }
    
    void nl(){
        sb.append(NL);
    }
    
    StringBuilder getStringBuilder(){
        return sb;
    }

}
